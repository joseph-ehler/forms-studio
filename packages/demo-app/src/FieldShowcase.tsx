import { useForm } from 'react-hook-form';
import { zodResolver } from '@hookform/resolvers/zod';
import { z } from 'zod';
import { Stack, Heading, Body, Card, AppProvider } from '@intstudio/ds';
import {
  TextField,
  EmailField,
  EmailEnhancedField,
  NumberField,
  UrlField,
  TelField,
  TextareaField,
  CheckboxField,
  DateField,
  TimeField,
  DateTimeField,
  ColorField,
  FileField,
  RangeField,
  SliderField,
  RatingField,
  TagInputField,
  ToggleField,
  MultiSelectField,
  LocationField,
  RangeCompositeField,
  SignatureField,
} from '@intstudio/forms/fields';
import { SelectField } from '../../forms/src/fields/SelectField/SelectField';

// Validation schema
const schema = z.object({
  name: z.string().min(2, 'Name must be at least 2 characters'),
  email: z.string().email('Invalid email address'),
  emailEnhanced: z.string().email('Invalid email address').optional().or(z.literal('')),
  age: z.coerce.number().min(18, 'Must be 18 or older'),
  website: z.string().url('Must be a valid URL').optional().or(z.literal('')),
  phone: z.string().optional(),
  bio: z.string().optional(),
  country: z.string().optional(),
  terms: z.boolean().refine((val) => val === true, 'You must accept the terms'),
  birthdate: z.string().optional(),
  meetingTime: z.string().optional(),
  appointment: z.string().optional(),
  favoriteColor: z.string().optional(),
  resume: z.any().optional(),
  volume: z.coerce.number().optional(),
  brightness: z.coerce.number().optional(),
  rating: z.coerce.number().optional(),
  tags: z.array(z.string()).optional(),
  notifications: z.boolean().optional(),
  interests: z.array(z.string()).optional(),
  location: z.object({ lat: z.coerce.number(), lng: z.coerce.number() }).optional(),
  priceRange: z.object({ min: z.coerce.number(), max: z.coerce.number() }).optional(),
  signature: z.string().optional(),
});

type FormData = z.infer<typeof schema>;

